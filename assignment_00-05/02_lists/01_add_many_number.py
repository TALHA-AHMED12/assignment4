def sum_of_numbers(numbers):
    # Calculate the sum of the numbers in the list
    return sum(numbers)

# Example usage
numbers_list = [1, 2, 3, 4, 5]
result = sum_of_numbers(numbers_list)
print("The sum of the numbers is:", result)

# This program defines a function that takes a list of numbers as input and returns their sum. It uses the built-in `sum` function to calculate the total. The example usage demonstrates how to call the function with a list of numbers and print the result.