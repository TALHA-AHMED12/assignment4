def subtract_seven(num):
    return num - 7

def main():
    number = int(input("Enter a number: "))
    result = subtract_seven(number)
    print("Result after subtracting 7:", result)

main()
# This program defines a function to subtract 7 from a number and then interacts with the user to get a number, perform the subtraction, and print the result. The main function is called to execute the program.